import pymbolic.primitives as p


def test_pattern_match():
    from pymbolic import var

    x = var("x")
    xp1 = (x+1)
    u = xp1**5 + x

    match u:
        case p.Sum((p.Power(base, exp), other_term)):
            assert base is xp1
            assert exp == 5
            assert other_term is x

        case _:
            raise AssertionError()


